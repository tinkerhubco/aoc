enum PlayerOption {
  Rock = 'X',
  Paper = 'Y',
  Scissors = 'Z',
}

enum PlayerOptionPart2 {
  ToLose = 'X',
  ToDraw = 'Y',
  ToWin = 'Z',
}

enum Result {
  Lost = 0,
  Draw = 3,
  Won = 6,
}

enum ShapeScore {
  Rock = 1,
  Paper = 2,
  Scissors = 3,
}

const getResultPart1 = (opponent: string, player: string) => {
  if (player === PlayerOption.Paper) {
    if (opponent === OpponentOption.Paper) {
      return Result.Draw + ShapeScore.Paper;
    }
    if (opponent === OpponentOption.Rock) {
      return Result.Won + ShapeScore.Paper;
    }
    if (opponent === OpponentOption.Scissors) {
      return Result.Lost + ShapeScore.Paper;
    }
  }

  if (player === PlayerOption.Rock) {
    if (opponent === OpponentOption.Rock) {
      return Result.Draw + ShapeScore.Rock;
    }
    if (opponent === OpponentOption.Paper) {
      return Result.Lost + ShapeScore.Rock;
    }
    if (opponent === OpponentOption.Scissors) {
      return Result.Won + ShapeScore.Rock;
    }
  }

  if (player === PlayerOption.Scissors) {
    if (opponent === OpponentOption.Scissors) {
      return Result.Draw + ShapeScore.Scissors;
    }
    if (opponent === OpponentOption.Rock) {
      return Result.Lost + ShapeScore.Scissors;
    }
    if (opponent === OpponentOption.Paper) {
      return Result.Won + ShapeScore.Scissors;
    }
  }

  return 0;
};

